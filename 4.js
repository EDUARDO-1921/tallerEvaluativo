function invertido(arreglo) {
    let arregloinvertido= [];
    for (let i = arreglo.length - 1; i >= 0; i--) {
        arregloinvertido.push(arreglo[i]);
}
    return invertido;  
}

let numeros = [10,9,3,2,1];
console.log(invertido(numeros)); 

let num = [10,30,40,];
console.log(invertido(num))